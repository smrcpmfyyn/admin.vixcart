package com.vixcart.admin.resp.mod;

/**
 * @company techvay
 * @author rifaie
 */
public class SuperUserType {
    private final String id;
    private final String type;

    public SuperUserType(String id, String type) {
        this.id = id;
        this.type = type;
    }

    public String getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    @Override
    public String toString() {
        return "{\"id\":\"" + id + "\", \"type\":\"" + type + "\"}";
    }
    
    
}
